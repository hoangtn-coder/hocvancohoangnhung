package hocvan.entity;

public class SideWidget {
	String imageWidget;
	String titleWidget;
	String id;
	
	public String getImageWidget() {
		return imageWidget;
	}
	public void setImageWidget(String imageWidget) {
		this.imageWidget = imageWidget;
	}
	public String getTitleWidget() {
		return titleWidget;
	}
	public void setTitleWidget(String titleWidget) {
		this.titleWidget = titleWidget;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
}
